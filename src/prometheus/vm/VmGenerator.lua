-- === Anti-Debugging ===

function VMGenerator:generateAntiDebug(names)
    return string.format([[

-- Anti-debugging checks
local function __CHECK_DEBUG__()
    -- Check for debug library
    if debug and debug.getinfo then
        error("Debugging detected", 0)
    end
    
    -- Check for common debugger functions
    if getfenv or setfenv then
        error("Environment manipulation detected", 0)
    end
    
    -- Timing check
    local start = os.clock()
    for i = 1, 1000 do end
    if os.clock() - start > 0.1 then
        error("Execution timing anomaly", 0)
    end
end

-- Run checks periodically
math.randomseed(os.time())
if math.random(1, 10) > 5 then
    __CHECK_DEBUG__()
end
]])
end

