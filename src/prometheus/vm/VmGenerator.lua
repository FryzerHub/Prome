-- prometheus/vm/generator.lua
-- VM Runtime Generator - Creates secure VM runtime with embedded bytecode

local VMGenerator = {}
VMGenerator.__index = VMGenerator

function VMGenerator.new(config)
    local self = setmetatable({}, VMGenerator)
    self.config = config or {}
    return self
end

function VMGenerator:generateFromAST(ast, sourceCode)
    -- Compile AST to bytecode using VMCompiler
    local VMCompiler = require("prometheus.vm.Compiler")
    local compiler = VMCompiler:new()
    
    -- Compile AST to bytecode
    local bytecode = compiler:compile(ast)
    
    -- Generate VM runtime with the bytecode
    return self:generate(bytecode, self.config)
end

function VMGenerator:generate(bytecode, config)
    config = config or self.config
    
    -- Security settings
    local obfuscateVM = config.obfuscateVMRuntime ~= false
    local compressBytecode = config.compressBytecode ~= false
    local encryptBytecode = config.encryptBytecode ~= false
    local shuffleOpcodes = config.shuffleOpcodes ~= false
    local antiDebug = config.antiDebug ~= false
    
    -- Apply opcode shuffling if enabled
    local opcodeMapping = nil
    if shuffleOpcodes then
        bytecode, opcodeMapping = self:shuffleOpcodes(bytecode)
    end
    
    -- Encrypt bytecode if enabled
    local encryptionKey = nil
    if encryptBytecode then
        encryptionKey = math.random(0x1000, 0xFFFF)
        bytecode = self:encryptBytecode(bytecode, encryptionKey)
    end
    
    -- Generate VM runtime
    local vmRuntime = self:generateVMRuntime(obfuscateVM, encryptionKey, antiDebug)
    
    -- Serialize bytecode
    local serialized = self:serializeBytecode(bytecode, compressBytecode)
    
    -- Combine into final output
    local output = string.format([[
-- ================================================================
-- VM-Protected Lua Script
-- Generated by Prometheus VM Obfuscator
-- WARNING: Unauthorized modification will cause execution failure
-- ================================================================

%s

-- Execute bytecode
local __BYTECODE__ = %s
return __VM_EXECUTE__(__BYTECODE__)
]], vmRuntime, serialized)
    
    -- Apply final obfuscation to entire output if enabled
    if obfuscateVM then
        output = self:finalObfuscation(output)
    end
    
    return output
end

-- === Opcode Shuffling ===

function VMGenerator:shuffleOpcodes(bytecode)
    local opcodes = bytecode.opcodes
    local mapping = {}
    local shuffled = {}
    
    -- Create list of opcodes
    local opcodeList = {}
    for name, value in pairs(opcodes) do
        table.insert(opcodeList, {name = name, value = value})
    end
    
    -- Shuffle opcodes
    for i = #opcodeList, 2, -1 do
        local j = math.random(1, i)
        opcodeList[i], opcodeList[j] = opcodeList[j], opcodeList[i]
    end
    
    -- Create mapping
    for newValue, entry in ipairs(opcodeList) do
        mapping[entry.value] = newValue - 1
        shuffled[entry.name] = newValue - 1
    end
    
    -- Remap instructions
    for _, instr in ipairs(bytecode.instructions) do
        instr.opcode = mapping[instr.opcode] or instr.opcode
    end
    
    bytecode.opcodes = shuffled
    
    return bytecode, mapping
end

-- === Bytecode Encryption ===

function VMGenerator:encryptBytecode(bytecode, key)
    -- Encrypt instruction opcodes and operands
    for _, instr in ipairs(bytecode.instructions) do
        instr.opcode = self:xorEncrypt(instr.opcode, key)
        instr.operand = self:xorEncrypt(instr.operand, key)
    end
    
    -- Encrypt constants
    for i, const in ipairs(bytecode.constants) do
        if type(const) == "number" then
            bytecode.constants[i] = const + key
        end
    end
    
    bytecode.encrypted = true
    bytecode.key = key
    
    return bytecode
end

function VMGenerator:xorEncrypt(value, key)
    return bit32 and bit32.bxor(value, key) or value
end

-- === Bytecode Serialization ===

function VMGenerator:serializeBytecode(bytecode, compress)
    local parts = {}
    
    -- Serialize instructions
    local instructions = {}
    for _, instr in ipairs(bytecode.instructions) do
        table.insert(instructions, string.format("{%d,%d,%d}", 
            instr.opcode, instr.operand, instr.aux or 0))
    end
    
    -- Serialize constants
    local constants = {}
    for _, const in ipairs(bytecode.constants) do
        if type(const) == "string" then
            table.insert(constants, string.format("%q", const))
        elseif type(const) == "number" then
            table.insert(constants, tostring(const))
        elseif type(const) == "boolean" then
            table.insert(constants, tostring(const))
        else
            table.insert(constants, "nil")
        end
    end
    
    local result = string.format([[{
i={%s},
c={%s},
e=%s,
k=%s,
v="%s"
}]], 
        table.concat(instructions, ","),
        table.concat(constants, ","),
        bytecode.encrypted and "true" or "false",
        bytecode.key or "nil",
        bytecode.version or "1.0.0"
    )
    
    if compress then
        result = self:compressString(result)
    end
    
    return result
end

function VMGenerator:compressString(str)
    -- Base64 encode for compression
    local encoded = self:base64Encode(str)
    return string.format('__VM_DECODE__(%q)', encoded)
end

function VMGenerator:base64Encode(data)
    local b64chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/'
    return ((data:gsub('.', function(x) 
        local r,b='',x:byte()
        for i=8,1,-1 do r=r..(b%2^i-b%2^i/2>0 and '1' or '0') end
        return r;
    end)..'0000'):gsub('%d%d%d?%d?%d?%d?', function(x)
        if (#x < 6) then return '' end
        local c=0
        for i=1,6 do c=c+(x:sub(i,i)=='1' and 2^(6-i) or 0) end
        return b64chars:sub(c+1,c+1)
    end)..({ '', '==', '=' })[#data%3+1])
end

-- === VM Runtime Generation ===

function VMGenerator:generateVMRuntime(obfuscate, encryptionKey, antiDebug)
    -- Generate variable names
    local names = obfuscate and self:generateObfuscatedNames() or self:generateNormalNames()
    
    local vmCode = string.format([[
-- VM Runtime
local %s = {}  -- Stack
local %s = 0   -- Stack pointer
local %s = {}  -- Locals
local %s = _G  -- Globals

-- Base64 decoder
local function __VM_DECODE__(encoded)
    local b64chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/'
    local decoded = {}
    encoded = encoded:gsub('[^'..b64chars..'=]', '')
    encoded:gsub('.', function(c)
        if c ~= '=' then
            local b = b64chars:find(c) - 1
            table.insert(decoded, b)
        end
    end)
    local result = ''
    for i = 1, #decoded, 4 do
        local a, b, c, d = decoded[i], decoded[i+1], decoded[i+2], decoded[i+3]
        if a and b then
            result = result .. string.char(a * 4 + math.floor((b or 0) / 16))
            if c then
                result = result .. string.char(((b or 0) %% 16) * 16 + math.floor((c or 0) / 4))
                if d then
                    result = result .. string.char(((c or 0) %% 4) * 64 + (d or 0))
                end
            end
        end
    end
    return loadstring("return " .. result)()
end

-- Stack operations
local function %s(v)  -- push
    %s = %s + 1
    %s[%s] = v
end

local function %s()  -- pop
    local v = %s[%s]
    %s[%s] = nil
    %s = %s - 1
    return v
end

local function %s()  -- peek
    return %s[%s]
end

local function %s()  -- dup
    %s(%s())
end
]], 
        names.stack, names.sp, names.locals, names.globals,
        names.push, names.sp, names.sp, names.stack, names.sp,
        names.pop, names.stack, names.sp, names.stack, names.sp, names.sp, names.sp,
        names.peek, names.stack, names.sp,
        names.dup, names.push, names.peek
    )
    
    -- Add anti-debugging if enabled
    if antiDebug then
        vmCode = vmCode .. self:generateAntiDebug(names)
    end
    
    -- Add main executor
    vmCode = vmCode .. string.format([[

-- Main VM executor
function __VM_EXECUTE__(%s)
    local %s = %s.i      -- Instructions
    local %s = %s.c      -- Constants
    local %s = %s.e      -- Encrypted flag
    local %s = %s.k or 0 -- Encryption key
    
    local %s = 1  -- Program counter
    local %s = false  -- Halted flag
]], 
        names.bytecode,
        names.instructions, names.bytecode,
        names.constants, names.bytecode,
        names.encrypted, names.bytecode,
        names.key, names.bytecode,
        names.pc, names.halted
    )
    
    -- Add decryption if needed
    if encryptionKey then
        vmCode = vmCode .. string.format([[
    
    -- Decrypt bytecode if encrypted
    if %s then
        for _, %s in ipairs(%s) do
            %s[1] = bit32 and bit32.bxor(%s[1], %s) or %s[1]
            %s[2] = bit32 and bit32.bxor(%s[2], %s) or %s[2]
        end
        for %s, %s in ipairs(%s) do
            if type(%s) == "number" then
                %s[%s] = %s - %s
            end
        end
    end
]], 
            names.encrypted,
            names.instr, names.instructions,
            names.instr, names.instr, names.key, names.instr,
            names.instr, names.instr, names.key, names.instr,
            names.i, names.const, names.constants,
            names.const,
            names.constants, names.i, names.const, names.key
        )
    end
    
    -- Main execution loop
    vmCode = vmCode .. string.format([[
    
    -- Execution loop
    while not %s and %s <= #%s do
        local %s = %s[%s]
        local %s = %s[1]
        local %s = %s[2]
        
        -- Opcode dispatch
]], 
        names.halted, names.pc, names.instructions,
        names.instr, names.instructions, names.pc,
        names.opcode, names.instr,
        names.operand, names.instr
    )
    
    -- Generate opcode handlers
    vmCode = vmCode .. self:generateOpcodeHandlers(names, obfuscate)
    
    -- Close execution loop
    vmCode = vmCode .. string.format([[
        
        %s = %s + 1
    end
    
    -- Return result if available
    if %s > 0 then
        return %s()
    end
end
]], names.pc, names.pc, names.sp, names.pop)
    
    return vmCode
end

