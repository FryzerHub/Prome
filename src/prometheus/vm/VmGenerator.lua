function VMGenerator:randomName()
    local chars = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ"
    local name = "_"
    for i = 1, math.random(6, 10) do
        local idx = math.random(1, #chars)
        name = name .. chars:sub(idx, idx)
    end
    return name
end

-- === Final Obfuscation ===

function VMGenerator:finalObfuscation(code)
    -- Add junk code
    local junk = [[
-- Integrity verification
local __VERIFY__ = function(x) return x end
local __INIT__ = os.time()
]]
    
    return junk .. "\n" .. code
end

